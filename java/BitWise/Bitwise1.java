class Bitwise1 {
    public static void main(String[] args) {
        int a = 30;
        int b = 55;
        /*
         * Ternary operator : a shorthand way or writing an if-else statement.
         */
        String result = a > b ? a + " is greatest" : b + " is greatest";
        System.out.println(result);
    }

}